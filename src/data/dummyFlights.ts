import { Flight, FlightCabin } from "../types";

const now = new Date();
const h = (hours: number) => new Date(now.getTime() + hours * 3600000).toISOString();

const ECONOMY_PERKS = ["Standard seat", "Carry-on bag", "In-flight meal"];
const FIRST_CLASS_PERKS = ["Lie-flat seat", "Priority boarding", "Lounge access", "Gourmet dining", "Premium drinks", "Extra baggage"];
const PRIVATE_JET_PERKS = ["Full aircraft charter", "Flexible schedule", "Custom catering", "Private terminal", "Ground transport", "Dedicated crew", "No baggage limits"];

function makeCabins(basePrice: number, totalSeats: number): FlightCabin[] {
  const economyPrice = Math.max(500, Math.round(basePrice * 0.35 / 10) * 10);
  const firstPrice = Math.max(1500, Math.round(basePrice * 1.2 / 10) * 10);
  const privatePrice = Math.max(10000, Math.round(basePrice * 6 / 100) * 100);
  return [
    {
      class: "economy",
      label: "Economy",
      price: economyPrice,
      available_seats: Math.round(totalSeats * 0.7),
      perks: ECONOMY_PERKS,
    },
    {
      class: "first_class",
      label: "First Class",
      price: firstPrice,
      available_seats: Math.round(totalSeats * 0.2),
      perks: FIRST_CLASS_PERKS,
    },
    {
      class: "private_jet",
      label: "Private Jet",
      price: privatePrice,
      available_seats: Math.round(totalSeats * 0.1) || 1,
      perks: PRIVATE_JET_PERKS,
    },
  ];
}

function f(id: number, airline: string, flight_number: string, origin: string, destination: string, hoursFromNow: number, durationHours: number, price: number, seats: number): Flight {
  return {
    id,
    airline,
    flight_number,
    origin,
    destination,
    departure_time: h(hoursFromNow),
    arrival_time: h(hoursFromNow + durationHours),
    price,
    available_seats: seats,
    cabins: makeCabins(price, seats),
  };
}

